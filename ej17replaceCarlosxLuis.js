// Declara una variable nombres con el valor "Carlos, Ana, Carlos, María".
//  Usa replace para cambiar solo el primer "Carlos" por "Luis".

let nombres= 'Carlos, Ana, Carlos, María'

let nuevosNombres= nombres.replace('Carlos','Luis')

console.log(`Los antiguos nombres eran: ${nombres}`)
console.log(`Los nuevos nombres son: ${nuevosNombres}`)