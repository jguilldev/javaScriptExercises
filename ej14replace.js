// Método: replace
// Descripción:
// replace(viejo, nuevo) busca la primera aparición de un texto (viejo) en el string
//  y lo reemplaza por otro texto (nuevo).

// NOTA IMPORTANTE:Si el texto aparece varias veces en el string, 
// solo reemplazará la primera ocurrencia.

// Es útil para cambiar palabras o frases específicas en un string.

// Sintaxis básica:
let texto = "Hola mundo, mundo hermoso";
console.log(texto.replace("mundo", "planeta")); // "Hola planeta, mundo hermoso"
