// 1. Métodos: toUpperCase y toLowerCase
// Explicación:
// toUpperCase convierte un string a mayúsculas.
// toLowerCase convierte un string a minúsculas.

// Sintaxis:
let texto = "Hola Mundo";
console.log(texto.toUpperCase()); // "HOLA MUNDO"
console.log(texto.toLowerCase()); // "hola mundo"

// Declara una variable llamada pais con el valor "Colombia". Muestra el nombre del país en mayúsculas.

let pais = 'Colombia'

console.log(`El nombre del mi pais de origene es: ${pais.toUpperCase()}`)