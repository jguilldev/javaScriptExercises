// Lección 1: ¿Qué es una variable?
// 1. Concepto básico
// Una variable es un lugar donde puedes guardar datos para usarlos más tarde. Piensa en una
//  variable como una caja con una etiqueta (nombre) y un contenido (dato).

// En JavaScript, puedes crear variables usando las palabras clave:

// let: Se usa cuando necesitas que el valor cambie más adelante.
// const: Se usa para valores que no cambiarán nunca.
// var (evitaremos usarlo porque es una forma antigua).

// sintaxis 
let nombre = "Juan"; // Una variable que puede cambiar
const edad = 25; // Una variable fija (no cambia)

