// Método: toLowerCase
// Descripción:
// toLowerCase convierte todas las letras de un string a minúsculas.
// Es útil cuando necesitas normalizar texto para comparación o para mostrar información
//  de manera consistente.

// Sintaxis:
let texto = "HOLA MUNDO";
console.log(texto.toLowerCase()); // "hola mundo"

// Ejercicio
// Declara una variable llamada ciudad con el valor "Bogotá". Usa el método toLowerCase 
// para mostrar el nombre de la ciudad en minúsculas con un mensaje como:

// El nombre de la ciudad en minúsculas es: bogotá

const ciudad ="BOGOTA";

console.log(`El nombre de la ciudad en minusculas es: ${ciudad.toLowerCase()}`)
