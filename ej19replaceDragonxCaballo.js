// Declara una variable historia con el valor "Había una vez un dragón que vivía en una cueva".
//  Usa replace para cambiar "dragón" por "caballo".

let historia ='Había una vez un dragón que vivía en una cueva'

nuevaHistoria= historia.replace('dragón','caballo')

console.log(`La frase en la antigua historia era: ${historia}`)
console.log(`La frase en la nueva historia es: ${nuevaHistoria}`)